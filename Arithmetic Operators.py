#All Arithmetic Operation Use.

a = int(input("Enter a 1st number : "))
b = int(input("Enter a 2nd number : "))

add = a + b
print("Addition of two number : ",add)

sub = a - b
print("SUbstraction of two number : ",sub)

mul = a * b
print("Multiplication of two number : ",mul)

div = a / b
print("Division of two number ",div)

modu = a % b
print("Modulus two number : ",modu)

floor = a // b
print("Floor two number : ",floor)

expo = a ** b
print("Exponent of two number : ",expo)
