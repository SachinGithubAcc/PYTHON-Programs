#All Arithmetic Operation Use.

a = int(input("Enter a number : "))
b = int(input("Enter b number : "))

less = a < b
print("Less than number : ",less)

greater = a > b
print("Greater number : ",greater)

equal = a == b
print("Equal number : ",equal)

Not_eq = a != b
print("Not Equal number : ",Not_eq)

gret_equ = a >= b
print("Greater than or equal : ",gret_equ)

less_eq = a <= b
print("Less than or equal : ",less_eq)